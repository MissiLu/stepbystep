// Использование переменной.

#include <iostream>
using namespace std;

int main ()
{
    int length; //объявим переменную
    int width;
    int size;
    length=7; //присваиваем ей значение 7
    width=100;
    size=length+width;
    cout<<"Длина равна ";
    cout<<size;//выведем значение переменной
    return 0;
}
