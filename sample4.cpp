//интерактивная площадь
#include <iostream>
using namespace std;

int main()
{
    int length, width;
    cout<<"Введите длину";
    cin>>length;/* запрашивает длину и сохраняет значение
    в переменной  length;*/
    
    cout<<"Введите ширину";
    cin>>width;

    cout<<"Площадь равна"<<length*width;

    return 0;
    

}